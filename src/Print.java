public class Print {
    public static void main(String[] args) {
        //숫자 출력 및 연산 가능
        System.out.println(1+3.5);

        //문자열과 문자의 차이 "", ''의 차이 발생
        System.out.println("A"+3);
        System.out.println('A'+3);

        //문자열 출력 및 편집 방법
        System.out.println("나는" + " \"자바\"야.");
        System.out.println("나는" + "\n자바야.");

    }
}
